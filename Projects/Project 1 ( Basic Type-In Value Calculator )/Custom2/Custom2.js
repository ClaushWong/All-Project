function calculation(a,b){
  var c = Number(a) + Number(b);
  document.getElementById("1").value = c;
  var d = Number(a) - Number(b);
  document.getElementById("2").value = d;
  var e = Number(a) * Number(b);
  document.getElementById("3").value = e;
  var f = Number(a) / Number(b);
  document.getElementById("4").value = f;
}
